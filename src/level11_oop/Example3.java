package level11_oop;

public class Example3 {
    public static void main(String[] args) {

        System.out.println(10 / 0.1);

        System.out.println(10 / 0.01);

        System.out.println(10 / 0.000000001); // 1 * 10 ^ 10

        System.out.println(10 / 0.00000000000000000000000000000000000000000000000000001);

        System.out.println(10 / 0.0);

        // System.out.println(10 / 0);

        System.out.println(-10 / 0.0);

        System.out.println((10 / 0.0) * 90);

    }
}
